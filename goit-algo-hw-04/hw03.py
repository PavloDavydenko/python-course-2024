"""
–†–æ–∑—Ä–æ–±—ñ—Ç—å —Å–∫—Ä–∏–ø—Ç, —è–∫–∏–π –ø—Ä–∏–π–º–∞—î —à–ª—è—Ö –¥–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó –≤ —è–∫–æ—Å—Ç—ñ –∞—Ä–≥—É–º–µ–Ω—Ç—É –∫–æ–º–∞–Ω–¥–Ω–æ–≥–æ —Ä—è–¥–∫–∞ —ñ –≤—ñ–∑—É–∞–ª—ñ–∑—É—î —Å—Ç—Ä—É–∫—Ç—É—Ä—É —Ü—ñ—î—ó –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó, 
–≤–∏–≤–æ–¥—è—á–∏ —ñ–º–µ–Ω–∞ –≤—Å—ñ—Ö –ø—ñ–¥–¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ–π —Ç–∞ —Ñ–∞–π–ª—ñ–≤. –î–ª—è –∫—Ä–∞—â–æ–≥–æ –≤—ñ–∑—É–∞–ª—å–Ω–æ–≥–æ —Å–ø—Ä–∏–π–Ω—è—Ç—Ç—è, —ñ–º–µ–Ω–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ–π —Ç–∞ —Ñ–∞–π–ª—ñ–≤ –º–∞—é—Ç—å –≤—ñ–¥—Ä—ñ–∑–Ω—è—Ç–∏—Å—è –∑–∞ –∫–æ–ª—å–æ—Ä–æ–º.

–í–∏–º–æ–≥–∏ –¥–æ –∑–∞–≤–¥–∞–Ω–Ω—è:
1. –°—Ç–≤–æ—Ä—ñ—Ç—å –≤—ñ—Ä—Ç—É–∞–ª—å–Ω–µ –æ—Ç–æ—á–µ–Ω–Ω—è Python –¥–ª—è —ñ–∑–æ–ª—è—Ü—ñ—ó –∑–∞–ª–µ–∂–Ω–æ—Å—Ç–µ–π –ø—Ä–æ—î–∫—Ç—É.
2. –°–∫—Ä–∏–ø—Ç –º–∞—î –æ—Ç—Ä–∏–º—É–≤–∞—Ç–∏ —à–ª—è—Ö –¥–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó —è–∫ –∞—Ä–≥—É–º–µ–Ω—Ç –ø—Ä–∏ –∑–∞–ø—É—Å–∫—É. –¶–µ–π —à–ª—è—Ö –≤–∫–∞–∑—É—î, –¥–µ –∑–Ω–∞—Ö–æ–¥–∏—Ç—å—Å—è –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—è, —Å—Ç—Ä—É–∫—Ç—É—Ä—É —è–∫–æ—ó –ø–æ—Ç—Ä—ñ–±–Ω–æ –≤—ñ–¥–æ–±—Ä–∞–∑–∏—Ç–∏.
3. –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è –±—ñ–±–ª—ñ–æ—Ç–µ–∫–∏ colorama –¥–ª—è —Ä–µ–∞–ª—ñ–∑–∞—Ü—ñ—ó –∫–æ–ª—å–æ—Ä–æ–≤–æ–≥–æ –≤–∏–≤–µ–¥–µ–Ω–Ω—è.
4. –°–∫—Ä–∏–ø—Ç –º–∞—î –∫–æ—Ä–µ–∫—Ç–Ω–æ –≤—ñ–¥–æ–±—Ä–∞–∂–∞—Ç–∏ —è–∫ —ñ–º–µ–Ω–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ–π, —Ç–∞–∫ —ñ —ñ–º–µ–Ω–∞ —Ñ–∞–π–ª—ñ–≤, –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—é—á–∏ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–∏–π —Å–ø–æ—Å—ñ–± –æ–±—Ö–æ–¥—É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ–π (–º–æ–∂–Ω–∞, –∑–∞ –±–∞–∂–∞–Ω–Ω—è–º, –≤–∏–∫–æ—Ä–∏—Å—Ç–∞—Ç–∏ –Ω–µ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–∏–π —Å–ø–æ—Å—ñ–±).
5. –ü–æ–≤–∏–Ω–Ω–∞ –±—É—Ç–∏ –ø–µ—Ä–µ–≤—ñ—Ä–∫–∞ —Ç–∞ –æ–±—Ä–æ–±–∫–∞ –ø–æ–º–∏–ª–æ–∫, –Ω–∞–ø—Ä–∏–∫–ª–∞–¥, —è–∫—â–æ –≤–∫–∞–∑–∞–Ω–∏–π —à–ª—è—Ö –Ω–µ —ñ—Å–Ω—É—î –∞–±–æ –≤—ñ–Ω –Ω–µ –≤–µ–¥–µ –¥–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó.
"""


from colorama import init, Fore
import sys
from pathlib import Path


def folder_info(directory: Path, prefix='', nesting_level=0):

    # –í–∏–≤–æ–¥–∏–º–æ —ñ–º'—è –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó –≤—ñ–¥ —è–∫–æ—ó –±—É–¥—É—î–º–æ –¥–µ—Ä–µ–≤–æ
    if nesting_level == 0:
        print(Fore.MAGENTA + "üì¶ " + directory.name)

    # –û—Ç—Ä–∏–º—É—î–º–æ –≤—ñ–¥—Å–æ—Ä—Ç–æ–≤–∞–Ω–∏–π —Å–ø–∏—Å–æ–∫ –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ —É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó
    items = sorted(directory.iterdir(), key=lambda item: item.name)

    # –Ü–¥–µ–º –ø–æ –∫–æ–∂–Ω–æ–º—É –µ–ª–µ–º–µ–Ω—Ç—É –≤—ñ–¥—Å–æ—Ä—Ç–æ–≤–∞–Ω–æ–≥–æ —Å–ø–∏—Å–∫—É
    for i, entry in enumerate(items):
        if entry.is_dir():

            # –Ø–∫—â–æ –µ–ª–µ–º–µ–Ω—Ç –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—è - –¥—Ä—É–∫—É—î–º–æ —á–∞—Å—Ç–∏–Ω—É –¥–µ—Ä–µ–≤–∞ —Ç–∞ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ –≤–∏–∫–ª–∏–∫–∞—î–º–æ —Ñ—É–Ω–∫—Ü—ñ—é —â–µ —Ä–∞–∑
            if i == len(items) - 1:
                print(Fore.LIGHTYELLOW_EX + prefix + " ‚îó‚îÅüìÇ " + Fore.MAGENTA + entry.name)
                folder_info(entry, Fore.LIGHTYELLOW_EX + prefix + "    ", nesting_level + 1)
            else:
                print(Fore.LIGHTYELLOW_EX + prefix + " ‚î£‚îÅüìÇ " + Fore.MAGENTA + entry.name)
                folder_info(entry, Fore.LIGHTYELLOW_EX + prefix + " ‚îÉ   ", nesting_level + 1)

        else:

            # –Ø–∫—â–æ –µ–ª–µ–º–µ–Ω—Ç —Ñ–∞–π–ª - –¥—Ä—É–∫—É—î–º–æ —á–∞—Å—Ç–∏–Ω—É –¥–µ—Ä–µ–≤–∞
            if i == len(items) - 1:
                print(Fore.LIGHTYELLOW_EX + prefix + " ‚îó‚îÅüìú " + Fore.GREEN + entry.name)
            else:
                print(Fore.LIGHTYELLOW_EX + prefix + " ‚î£‚îÅüìú " + Fore.GREEN + entry.name)


def main():

    # –°–∫–∏–¥–∞—î–º–æ –Ω–∞–ª–∞—à—Ç—É–≤–∞–Ω–Ω—è –¥–ª—è colorama –ø—ñ—Å–ª—è –∫–æ–∂–Ω–æ–≥–æ –≤–∏–∫–ª–∏–∫—É
    init(autoreset=True)

    # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –∞—Ä–≥—É–º–µ–Ω—Ç—ñ–≤
    if len(sys.argv) == 2:
        directory = Path(sys.argv[1])
        print(directory)

    # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ —á–∏ —ñ—Å–Ω—É—î –≤–∫–∞–∑–∞–Ω–∏–π —à–ª—è—Ö
    if not directory.exists():
        print("No such directory.")
        sys.exit(1)

    # –í–∏–≤–æ–¥–∏–º–æ –∑–∞–≥–æ–ª–æ–≤–æ–∫ –¥–µ—Ä–µ–≤–∞
    print(Fore.YELLOW + "Directory structure:")

    # –í–∏–∫–ª–∏–∫–∞—î–º–æ —Ñ—É–Ω–∫—Ü—ñ—é –ø–æ–±—É–¥–æ–≤–∏ –¥–µ—Ä–µ–≤–∞
    folder_info(directory)


if __name__ == "__main__":
    main()
